    EXEC('
CREATE FUNCTION [dbo].[ds_fnRandomWordBetween](@LowerBound INT, @UpperBound INT)
RETURNS INT
AS
BEGIN
    DECLARE @TMP FLOAT;
    SELECT @TMP = (SELECT MyRAND FROM ds_vwGetRandom);
    RETURN CAST(@TMP* (@UpperBound - @LowerBound) + @LowerBound AS INT);
END
   ')   ;


