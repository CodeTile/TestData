CREATE Function [dbo].[ds_fnRandomWords](@wordsRequired int)
returns VARCHAR(255)
as
BEGIN
    Declare @result nvarchar(4000) =''
    DECLARE @counter INT SET @counter = 0 
		
    WHILE @counter < @wordsRequired
    BEGIN 
       set @result= @result+' ' + dbo.ds_fnRandomWord()
       SET @counter = @counter + 1
       if(@counter %14 =0)
             set @result= @result+'. '
    END
return 
(
	

select ltrim(rTrim(@result)) as [Words]
)
END;
   

